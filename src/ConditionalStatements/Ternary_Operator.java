package ConditionalStatements;

public class Ternary_Operator {
    public static void main(String[] args){
        int a=15;
        int b=8;

        int max_of_both_numbers=0;

        max_of_both_numbers=(a>b)?a:b;

        System.out.println("Maximum of both numbers = "+max_of_both_numbers);
    }
}
